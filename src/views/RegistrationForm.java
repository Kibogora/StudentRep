    private void programmeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_programmeMouseClicked
        RepDao rd = new RepDao();
        if(programme.getSelectedIndex() == -1){
            programme.removeAllItems();
            List<AcademicStructure> programmes = rd.getProgrammes();
            for(AcademicStructure as: programmes){
                programme.addItem(as.getAcaName());
            }
        }
    }//GEN-LAST:event_programmeMouseClicked

    private void programmeItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_programmeItemStateChanged
        RepDao rd = new RepDao();
        if(programme.getSelectedIndex() >= 0){
            faculty.removeAllItems();
            String selectedProgramme = (String) programme.getSelectedItem();
            List<String> facultyMastersList = rd.getFacultyMasters(selectedProgramme);
            for(String s: facultyMastersList){
                faculty.addItem(s);
            }
        }
    }//GEN-LAST:event_programmeItemStateChanged

    private void facultyItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_facultyItemStateChanged
        major.removeAllItems();
        String selectedFaculty = (String) faculty.getSelectedItem();
        if(programme.getSelectedIndex() == -1 || programme.getSelectedItem().equals("Undergraduate")){
            major.setEnabled(true);       
            RepDao rd = new RepDao();
            List<String> majors = rd.getMajor(selectedFaculty);
            for(String s: majors){
                major.addItem(s);
            }
        }else if(programme.getSelectedItem().equals("Graduate")){
            major.setEnabled(false);
        }
    }//GEN-LAST:event_facultyItemStateChanged

    private void registerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerButtonActionPerformed
        int age = validateDob();
        int idLength = validateID();
        if(age < 20){
            JOptionPane.showMessageDialog(null, "You have to be atleast 20 years of age !!");
        }
        if(idLength != 5){
            JOptionPane.showMessageDialog(null, "ID must be 5 characters !!");
        }
        
        if(age >= 20 && idLength == 5){
            StudentRepresentative st = new StudentRepresentative();
            st.setRepId(id.getText());
            st.setRepFullName(fullName.getText());
            st.setRepDob(((JTextField)birthDate.getDateEditor().getUiComponent()).getText());
            st.setRepMajorMasters(major.getSelectedItem().toString());
            RepDao dao = new RepDao();
            dao.saveRepresentative(st);
            JOptionPane.showMessageDialog(null, "Student Represenntative Saved !!!");            
        }
    }//GEN-LAST:event_registerButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        String studId = JOptionPane.showInputDialog(null, "Enter Representative ID: ");
        int deleted = 0;
        RepDao rd = new RepDao();
        StudentRepresentative sr = rd.getStud(studId);
        if(sr == null){
            JOptionPane.showMessageDialog(null, "ID doesn't exist !!!");         
        }else{
            deleted = rd.deleteStud(studId);
            if(deleted == 1){
                JOptionPane.showMessageDialog(null, "Student Representative Deleted !!!");        
            }
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        String studId = JOptionPane.showInputDialog(null, "Enter Representative ID: ");
        RepDao rd = new RepDao();
        StudentRepresentative sr = rd.getStud(studId);
        if(sr == null){
            JOptionPane.showMessageDialog(null, "ID doesn't exist !!!");         
        }else{
            id.setText(sr.getRepId());
            fullName.setText(sr.getRepFullName());
            birthDate.setDate(new Date(sr.getRepDob()));
            major.setSelectedItem(sr.getRepMajorMasters());
        }
    }//GEN-LAST:event_updateButtonActionPerformed

