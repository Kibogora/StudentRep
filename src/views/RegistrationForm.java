    public int validateDob(){
        String dob = ((JTextField)birthDate.getDateEditor().getUiComponent()).getText();
        String[] myDate = dob.split("/");
        int age = 0;

        int day = Integer.parseInt(myDate[0]);
        int month = Integer.parseInt(myDate[1]);
        int year = Integer.parseInt(myDate[2]);
        
        LocalDate birthDate = LocalDate.of(year, month, day);
        LocalDate currentDate = LocalDate.now();
        
        age = Period.between(birthDate, currentDate).getYears();
        
        return age;
    }
    
    public int validateID(){
        return id.getText().length();
    }
